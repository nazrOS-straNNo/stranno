//! Шина событий nazrOS
//!
//! Pub/Sub система для взаимодействия между модулями.
//! Ни один модуль не обращается к другому напрямую — только через шину.

use chrono::Utc;
use serde::{Deserialize, Serialize};
use std::collections::HashMap;
use uuid::Uuid;

/// Тип обработчика события
pub type ОбработчикСобытия = Box<dyn Fn(&Event) + Send + Sync>;

/// Идентификатор подписки (для отписки)
pub type IdПодписки = Uuid;

/// Идентификатор модуля-отправителя
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum IdМодуля {
    Ядро,
    Руки,
    Мульт,
    Точки,
    Студия,
    Поле,
    Шина,
    Глюк,
    Яблочко,
    Колодец,
    Библиотека,
    Лавка,
    Клуб,
    Внешний(String),
}

impl std::fmt::Display for IdМодуля {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            IdМодуля::Ядро => write!(f, "ЯДРО"),
            IdМодуля::Руки => write!(f, "РУКИ"),
            IdМодуля::Мульт => write!(f, "МУЛЬТ"),
            IdМодуля::Точки => write!(f, "ТОЧКИ"),
            IdМодуля::Студия => write!(f, "СТУДИЯ"),
            IdМодуля::Поле => write!(f, "ПОЛЕ"),
            IdМодуля::Шина => write!(f, "ШИНА"),
            IdМодуля::Глюк => write!(f, "ГЛЮК"),
            IdМодуля::Яблочко => write!(f, "ЯБЛОЧКО"),
            IdМодуля::Колодец => write!(f, "КОЛОДЕЦ"),
            IdМодуля::Библиотека => write!(f, "БИБЛИОТЕКА"),
            IdМодуля::Лавка => write!(f, "ЛАВКА"),
            IdМодуля::Клуб => write!(f, "КЛУБ"),
            IdМодуля::Внешний(имя) => write!(f, "ВНЕШНИЙ:{}", имя),
        }
    }
}

/// Событие в шине
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Event {
    /// Уникальный ID события
    pub id: Uuid,
    /// Временная метка (мс с Unix epoch)
    pub время: i64,
    /// Источник события
    pub источник: IdМодуля,
    /// Тип события
    pub вид: EventKind,
}

impl Event {
    pub fn новое(источник: IdМодуля, вид: EventKind) -> Self {
        Self {
            id: Uuid::new_v4(),
            время: Utc::now().timestamp_millis(),
            источник,
            вид,
        }
    }
}

/// Типы событий платформы
#[derive(Debug, Clone, Serialize, Deserialize)]
#[non_exhaustive]
pub enum EventKind {
    // ─── Сцена ──────────────────────────────────────────
    /// Узел добавлен в граф
    НодДобавлен(Uuid),
    /// Узел удалён из графа
    НодУдалён(Uuid),
    /// Свойство узла изменено
    НодИзменён { id: Uuid, поле: String },
    /// Компонент изменён
    КомпонентИзменён {
        нод: Uuid, компонент: String
    },
    /// Выделение сменилось
    ВыделениеСменилось { выделено: Vec<Uuid> },

    // ─── Файлы ──────────────────────────────────────────
    /// Запрос сохранения сцены
    СохранитьСцену,
    /// Запрос загрузки сцены
    ЗагрузитьСцену(String), // путь к .нзр файлу
    /// Новая сцена
    НоваяСцена,
    /// Сцена сохранена (ответ)
    СценаСохранена { путь: String },

    // ─── Рендер ─────────────────────────────────────────
    /// Отправить задачу в рендер-очередь
    ОтправитьВРендер(ЗаданиеРендера),
    /// Рендер завершён
    РендерЗавершён { задание_id: Uuid },
    /// Прогресс рендера
    ПрогрессРендера {
        задание_id: Uuid, процент: f32
    },

    // ─── Undo/Redo ───────────────────────────────────────
    /// Отменить последнее действие
    Отменить,
    /// Повторить отменённое действие
    Повторить,

    // ─── UI ─────────────────────────────────────────────
    /// Сменить активный модуль
    СменитьМодуль(IdМодуля),
    /// Обновить статус-бар
    ОбновитьСтатус { текст: String },
    /// Обновить показатель памяти (для статус-бара "память мира 72%")
    ОбновитьПамять {
        использовано_мб: u64, всего_мб: u64
    },
    /// Обновить FPS
    ОбновитьFPS { fps: f32 },

    // ─── Аудио (ШИНА) ───────────────────────────────────
    /// Воспроизвести аудио-клип
    ВоспроизвестиАудио { клип_id: Uuid },
    /// Остановить воспроизведение
    ОстановитьАудио,
    /// Изменить темп (BPM)
    ИзменитьТемп(f32),

    // ─── Автосохранение ─────────────────────────────────
    /// Триггер автосохранения
    Автосохранение,
    /// Статус автосохранения (для отображения "autosave: 02:17 назад")
    АвтосохранениеВыполнено { минут_назад: u32 },
}

/// Задание на рендер
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ЗаданиеРендера {
    pub id: Uuid,
    pub сцена_id: Uuid,
    pub разрешение: (u32, u32),
    pub частота_кадров: u32,
    pub кадр_начало: u32,
    pub кадр_конец: u32,
    pub движок: ДвижокРендера,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum ДвижокРендера {
    /// Быстрый real-time превью
    Превью,
    /// Полный path-tracing рендер
    НазрРендер,
    /// Облачный рендер (отправляется на ферму)
    Облако,
}

/// Шина событий — центральный pub/sub
pub struct EventBus {
    /// Подписчики: EventKind-discriminant → список обработчиков
    подписчики: HashMap<String, Vec<(IdПодписки, ОбработчикСобытия)>>,
    /// История событий (для отладки)
    история: Vec<Event>,
    /// Лимит истории
    лимит_истории: usize,
}

impl EventBus {
    pub fn новая() -> Self {
        Self {
            подписчики: HashMap::new(),
            история: Vec::new(),
            лимит_истории: 1000,
        }
    }

    /// Подписаться на тип событий
    /// Возвращает ID подписки для последующей отписки
    pub fn подписаться(
        &mut self,
        ключ: impl Into<String>,
        обработчик: ОбработчикСобытия,
    ) -> IdПодписки {
        let id = Uuid::new_v4();
        self.подписчики
            .entry(ключ.into())
            .or_default()
            .push((id, обработчик));
        id
    }

    /// Отписаться по ID
    pub fn отписаться(&mut self, id: IdПодписки) {
        for список in self.подписчики.values_mut() {
            список.retain(|(sub_id, _)| *sub_id != id);
        }
    }

    /// Опубликовать событие
    pub fn опубликовать(&mut self, событие: Event) {
        let ключ = дискриминант_события(&событие.вид);

        // Сохранить в историю
        if self.история.len() >= self.лимит_истории {
            self.история.remove(0);
        }
        self.история.push(событие.clone());

        // Уведомить подписчиков
        if let Some(список) = self.подписчики.get(&ключ) {
            for (_, обработчик) in список {
                обработчик(&событие);
            }
        }

        // Уведомить подписчиков на ВСЕ события
        if let Some(список) = self.подписчики.get("*") {
            for (_, обработчик) in список {
                обработчик(&событие);
            }
        }
    }

    /// История последних N событий
    pub fn последние_события(&self, n: usize) -> &[Event] {
        let старт = self.история.len().saturating_sub(n);
        &self.история[старт..]
    }
}

impl std::fmt::Debug for EventBus {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("EventBus")
            .field("подписчики", &self.подписчики.keys().collect::<Vec<_>>())
            .field("событий_в_истории", &self.история.len())
            .finish()
    }
}

/// Строковый ключ для диспетчеризации событий
fn дискриминант_события(вид: &EventKind) -> String {
    match вид {
        EventKind::НодДобавлен(_) => "НодДобавлен",
        EventKind::НодУдалён(_) => "НодУдалён",
        EventKind::НодИзменён { .. } => "НодИзменён",
        EventKind::КомпонентИзменён { .. } => "КомпонентИзменён",
        EventKind::ВыделениеСменилось { .. } => "ВыделениеСменилось",
        EventKind::СохранитьСцену => "СохранитьСцену",
        EventKind::ЗагрузитьСцену(_) => "ЗагрузитьСцену",
        EventKind::НоваяСцена => "НоваяСцена",
        EventKind::СценаСохранена { .. } => "СценаСохранена",
        EventKind::ОтправитьВРендер(_) => "ОтправитьВРендер",
        EventKind::РендерЗавершён { .. } => "РендерЗавершён",
        EventKind::ПрогрессРендера { .. } => "ПрогрессРендера",
        EventKind::Отменить => "Отменить",
        EventKind::Повторить => "Повторить",
        EventKind::СменитьМодуль(_) => "СменитьМодуль",
        EventKind::ОбновитьСтатус { .. } => "ОбновитьСтатус",
        EventKind::ОбновитьПамять { .. } => "ОбновитьПамять",
        EventKind::ОбновитьFPS { .. } => "ОбновитьFPS",
        EventKind::ВоспроизвестиАудио { .. } => "ВоспроизвестиАудио",
        EventKind::ОстановитьАудио => "ОстановитьАудио",
        EventKind::ИзменитьТемп(_) => "ИзменитьТемп",
        EventKind::Автосохранение => "Автосохранение",
        EventKind::АвтосохранениеВыполнено { .. } => {
            "АвтосохранениеВыполнено"
        }
    }
    .to_string()
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::sync::{Arc, Mutex};

    #[test]
    fn подписка_и_получение_события() {
        let mut шина = EventBus::новая();
        let получено = Arc::new(Mutex::new(false));
        let получено_клон = получено.clone();

        шина.подписаться(
            "НодДобавлен",
            Box::new(move |_| {
                *получено_клон.lock().unwrap() = true;
            }),
        );

        let событие = Event::новое(IdМодуля::Руки, EventKind::НодДобавлен(Uuid::new_v4()));
        шина.опубликовать(событие);

        assert!(*получено.lock().unwrap());
    }

    #[test]
    fn история_событий_ограничена() {
        let mut шина = EventBus::новая();
        шина.лимит_истории = 5;

        for _ in 0..10 {
            шина.опубликовать(Event::новое(IdМодуля::Ядро, EventKind::Автосохранение));
        }

        assert!(шина.история.len() <= 5);
    }
}
