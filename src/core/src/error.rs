//! Типы ошибок nazrOS CORE

use thiserror::Error;
use uuid::Uuid;

/// Все ошибки ядра nazrOS
#[derive(Debug, Error)]
pub enum CoreError {
    // ─── Граф сцены ──────────────────────────────────────
    #[error("Узел не найден: {0}")]
    УзелНеНайден(Uuid),

    #[error("Компонент не найден на узле {нод}: {тип}")]
    КомпонентНеНайден { нод: Uuid, тип: String },

    #[error("Циклическая зависимость: узел {0} не может быть родителем самого себя")]
    ЦикличнаяЗависимость(Uuid),

    // ─── История действий ────────────────────────────────
    #[error("Нечего отменять — стек пуст")]
    НечегоОтменять,

    #[error("Нечего повторять — стек пуст")]
    НечегоПовторять,

    // ─── Файловые операции ───────────────────────────────
    #[error("Ошибка чтения файла: {путь} — {причина}")]
    ОшибкаЧтения {
        путь: String, причина: String
    },

    #[error("Ошибка записи файла: {путь} — {причина}")]
    ОшибкаЗаписи {
        путь: String, причина: String
    },

    #[error("Неверный формат файла: ожидался {ожидался}, получен {получен}")]
    НеверныйФормат {
        ожидался: String, получен: String
    },

    #[error("Неподдерживаемая версия формата: {0}")]
    НеподдерживаемаяВерсия(u32),

    #[error("Нарушена контрольная сумма файла")]
    НарушенаКонтрольнаяСумма,

    // ─── Плагины/модули ──────────────────────────────────
    #[error("Модуль не найден: {0}")]
    МодульНеНайден(String),

    #[error("Несовместимая версия API модуля {модуль}: требуется {требуется}, найдена {найдена}")]
    НесовместимыйAPI {
        модуль: String,
        требуется: u32,
        найдена: u32,
    },

    // ─── Память / GPU ────────────────────────────────────
    #[error("Недостаточно памяти GPU: требуется {требуется_мб} МБ, доступно {доступно_мб} МБ")]
    НедостаточноПамятиGPU {
        требуется_мб: u64, доступно_мб: u64
    },

    #[error("Ресурс GPU не найден: {0}")]
    РесурсGPUНеНайден(String),

    // ─── Сеть / Облако ───────────────────────────────────
    #[error("Ошибка соединения с облаком: {0}")]
    ОшибкаСоединения(String),

    #[error("Таймаут рендер-фермы")]
    ТаймаутРендераФермы,

    // ─── Общие ───────────────────────────────────────────
    #[error("Внутренняя ошибка ядра: {0}")]
    Внутренняя(String),

    #[error(transparent)]
    IO(#[from] std::io::Error),
}

impl CoreError {
    /// Является ли ошибка восстановимой?
    /// (некоторые ошибки требуют перезапуска, другие — нет)
    pub fn восстановимая(&self) -> bool {
        matches!(
            self,
            CoreError::УзелНеНайден(_)
                | CoreError::КомпонентНеНайден { .. }
                | CoreError::НечегоОтменять
                | CoreError::НечегоПовторять
                | CoreError::МодульНеНайден(_)
        )
    }

    /// Код ошибки для логов и телеметрии
    pub fn код(&self) -> &'static str {
        match self {
            CoreError::УзелНеНайден(_) => "CORE_001",
            CoreError::КомпонентНеНайден { .. } => "CORE_002",
            CoreError::ЦикличнаяЗависимость(_) => "CORE_003",
            CoreError::НечегоОтменять => "CORE_010",
            CoreError::НечегоПовторять => "CORE_011",
            CoreError::ОшибкаЧтения { .. } => "CORE_020",
            CoreError::ОшибкаЗаписи { .. } => "CORE_021",
            CoreError::НеверныйФормат { .. } => "CORE_022",
            CoreError::НеподдерживаемаяВерсия(_) => "CORE_023",
            CoreError::НарушенаКонтрольнаяСумма => "CORE_024",
            CoreError::МодульНеНайден(_) => "CORE_030",
            CoreError::НесовместимыйAPI { .. } => "CORE_031",
            CoreError::НедостаточноПамятиGPU { .. } => "CORE_040",
            CoreError::РесурсGPUНеНайден(_) => "CORE_041",
            CoreError::ОшибкаСоединения(_) => "CORE_050",
            CoreError::ТаймаутРендераФермы => "CORE_051",
            CoreError::Внутренняя(_) => "CORE_999",
            CoreError::IO(_) => "CORE_IO",
        }
    }
}
