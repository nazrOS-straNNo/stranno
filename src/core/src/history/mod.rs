use crate::scene::Node;
use uuid::Uuid;

/// Команда: добавить узел в граф сцены
pub struct КомандаДобавитьНод {
    узел: Option<Node>,
    добавленный_id: Option<Uuid>,
}

impl КомандаДобавитьНод {
    pub fn новая(узел: Node) -> Self {
        Self {
            узел: Some(узел),
            добавленный_id: None,
        }
    }
}

impl Команда for КомандаДобавитьНод {
    fn выполнить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(узел) = self.узел.take() {
            self.добавленный_id = Some(граф.добавить(узел));
        }
        Ok(())
    }

    fn отменить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(id) = self.добавленный_id {
            // Извлечь узел обратно для возможного повтора
            if let Some(узел) = граф.получить(id).cloned() {
                self.узел = Some(узел);
            }
            граф.удалить(id)?;
        }
        Ok(())
    }

    fn описание(&self) -> &str {
        "Добавить объект"
    }
}

/// Команда: удалить узел из графа
pub struct КомандаУдалитьНод {
    id: Uuid,
    сохранённый_узел: Option<Node>,
}

impl КомандаУдалитьНод {
    pub fn новая(id: Uuid) -> Self {
        Self {
            id,
            сохранённый_узел: None,
        }
    }
}

impl Команда for КомандаУдалитьНод {
    fn выполнить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(узел) = граф.получить(self.id).cloned() {
            self.сохранённый_узел = Some(узел);
        }
        граф.удалить(self.id)
    }

    fn отменить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(узел) = self.сохранённый_узел.take() {
            граф.добавить(узел);
        }
        Ok(())
    }

    fn описание(&self) -> &str {
        "Удалить объект"
    }
}

/// Команда: переместить узел
pub struct КомандаПереместитьНод {
    id: Uuid,
    старая_позиция: glam::Vec3,
    новая_позиция: glam::Vec3,
}

impl КомандаПереместитьНод {
    pub fn новая(id: Uuid, старая: glam::Vec3, новая: glam::Vec3) -> Self {
        Self {
            id,
            старая_позиция: старая,
            новая_позиция: новая,
        }
    }
}

impl Команда for КомандаПереместитьНод {
    fn выполнить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(узел) = граф.получить_мут(self.id) {
            узел.трансформ.позиция = self.новая_позиция;
            Ok(())
        } else {
            Err(CoreError::УзелНеНайден(self.id))
        }
    }

    fn отменить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(узел) = граф.получить_мут(self.id) {
            узел.трансформ.позиция = self.старая_позиция;
            Ok(())
        } else {
            Err(CoreError::УзелНеНайден(self.id))
        }
    }

    fn описание(&self) -> &str {
        "Переместить объект"
    }

    fn можно_объединить(&self, другая: &dyn Команда) -> bool {
        // Объединять последовательные перемещения одного объекта
        другая.описание() == "Переместить объект"
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::scene::SceneGraph;

    #[test]
    fn добавить_и_отменить() {
        let mut граф = SceneGraph::новый();
        let mut история = ИсторияДействий::новая(50);

        let узел = Node::новый("тест".to_string());
        let команда = Box::new(КомандаДобавитьНод::новая(узел));

        история.выполнить(команда, &mut граф).unwrap();
        assert_eq!(граф.количество_узлов(), 1);

        история.отменить(&mut граф).unwrap();
        assert_eq!(граф.количество_узлов(), 0);
    }

    #[test]
    fn добавить_отменить_повторить() {
        let mut граф = SceneGraph::новый();
        let mut история = ИсторияДействий::новая(50);

        история
            .выполнить(
                Box::new(КомандаДобавитьНод::новая(
                    Node::новый("а".to_string()),
                )),
                &mut граф,
            )
            .unwrap();
        assert_eq!(граф.количество_узлов(), 1);

        история.отменить(&mut граф).unwrap();
        assert_eq!(граф.количество_узлов(), 0);

        история.повторить(&mut граф).unwrap();
        assert_eq!(граф.количество_узлов(), 1);
    }

    #[test]
    fn лимит_истории_соблюдается() {
        let mut граф = SceneGraph::новый();
        let mut история = ИсторияДействий::новая(3);

        for i in 0..5 {
            история
                .выполнить(
                    Box::new(КомандаДобавитьНод::новая(
                        Node::новый(format!("узел_{}", i)),
                    )),
                    &mut граф,
                )
                .unwrap();
        }

        assert!(история.глубина_отмены() <= 3);
    }
}
