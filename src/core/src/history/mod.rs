//! История действий — Undo/Redo для nazrOS
//!
//! Command Pattern: каждое действие пользователя — команда
//! с методами `выполнить()` и `отменить()`.
//!
//! Все модули (РУКИ, МУЛЬТ, СТУДИЯ и т.д.) создают команды
//! и передают их в единую историю ядра.

use crate::error::CoreError;
use crate::scene::SceneGraph;

/// Трейт команды — любое обратимое действие пользователя.
///
/// Реализуется в каждом модуле для своих операций:
/// - РУКИ: ДобавитьВершину, ПеретащитьВершины, НанестиМазок...
/// - МУЛЬТ: УстановитьКлюч, ИзменитьКонтроллер...
/// - СТУДИЯ: ДобавитьДорожку, СдвинутьКлип...
pub trait Команда: Send + Sync {
    /// Выполнить действие
    fn выполнить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError>;

    /// Отменить действие (вернуть состояние до выполнения)
    fn отменить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError>;

    /// Описание действия для UI ("История действий")
    fn описание(&self) -> &str;

    /// Можно ли объединить эту команду с предыдущей?
    /// Например: несколько команд "переместить" при drag&drop
    /// объединяются в одну для удобства отмены.
    fn можно_объединить(&self, _другая: &dyn Команда) -> bool {
        false
    }
}

/// История действий — единый стек для всей платформы.
pub struct ИсторияДействий {
    /// Стек выполненных команд (для Ctrl+Z)
    стек_отмены: Vec<Box<dyn Команда>>,
    /// Стек отменённых команд (для Ctrl+Y / Ctrl+Shift+Z)
    стек_повтора: Vec<Box<dyn Команда>>,
    /// Максимальная глубина истории
    лимит: usize,
}

impl ИсторияДействий {
    /// Создать историю с заданным лимитом глубины
    pub fn новая(лимит: usize) -> Self {
        Self {
            стек_отмены: Vec::new(),
            стек_повтора: Vec::new(),
            лимит,
        }
    }

    /// Выполнить команду и добавить в историю
    pub fn выполнить(
        &mut self,
        mut команда: Box<dyn Команда>,
        граф: &mut SceneGraph,
    ) -> Result<(), CoreError> {
        команда.выполнить(граф)?;

        // Объединение с предыдущей командой (если применимо)
        if let Some(пред) = self.стек_отмены.last() {
            if команда.можно_объединить(пред.as_ref()) {
                // TODO: реализовать merge
            }
        }

        // Добавить в стек отмены
        self.стек_отмены.push(команда);

        // Очистить стек повтора (новое действие ломает ветку повтора)
        self.стек_повтора.clear();

        // Обрезать при превышении лимита (удаляем самые старые)
        if self.стек_отмены.len() > self.лимит {
            self.стек_отмены.remove(0);
        }

        Ok(())
    }

    /// Отменить последнее действие (Ctrl+Z)
    pub fn отменить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(mut команда) = self.стек_отмены.pop() {
            команда.отменить(граф)?;
            self.стек_повтора.push(команда);
            Ok(())
        } else {
            Err(CoreError::НечегоОтменять)
        }
    }

    /// Повторить отменённое действие (Ctrl+Y)
    pub fn повторить(&mut self, граф: &mut SceneGraph) -> Result<(), CoreError> {
        if let Some(mut команда) = self.стек_повтора.pop() {
            команда.выполнить(граф)?;
            self.стек_отмены.push(команда);
            Ok(())
        } else {
            Err(CoreError::НечегоПовторять)
        }
    }

    /// Можно ли отменить?
    pub fn можно_отменить(&self) -> bool {
        !self.стек_отмены.is_empty()
    }

    /// Можно ли повторить?
    pub fn можно_повторить(&self) -> bool {
        !self.стек_повтора.is_empty()
    }

    /// Глубина стека отмены
    pub fn глубина_отмены(&self) -> usize {
        self.стек_отмены.len()
    }

    /// Описания действий для панели "ИСТОРИЯ ДЕЙСТВИЙ" в UI
    pub fn история_для_ui(&self) -> Vec<&str> {
        self.стек_отмены
            .iter()
            .rev()
            .map(|к| к.описание())
            .collect()
    }

    /// Очистить всю историю (при загрузке новой сцены)
    pub fn очистить(&mut self) {
        self.стек_отмены.clear();
        self.стек_повтора.clear();
    }
}

impl std::fmt::Debug for ИсторияДействий {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("ИсторияДействий")
            .field("отмены", &self.стек_отмены.len())
            .field("повторов", &self.стек_повтора.len())
            .field("лимит", &self.лимит)
            .finish()
    }
}

